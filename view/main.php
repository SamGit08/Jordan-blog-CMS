  <h1 id="history">Jojo History</h1>
  <div>
    <img src="/Jordan-Blog-CMS/view/Images/michealquai54.jpg" alt="">
    <p>Nike Air Jordan est une ligne de chaussures de basket-ball de marque Nike, frappées du nom de Michael Jordan, ancien joueur des Chicago Bulls en NBA.<br><br>
    Michael Jordan a porté des Converse All-Star pendant ses saisons en NCAA. Puis, pendant son année rookie, il a porté des Nike Air Force One enfin, l'année suivante, des Air Jordan "One".<br><br>
    La Jordan Brand (la marque Jordan) a été créée en 1988, en partenariat avec Nike. Le Swoosh, qui est le logo de la firme, n’apparaît plus sur les produits de la marque Jordan à partir de la Air Jordan VII (1992), seul le Jumpman (logo) (en) y figure : cela est le prolongement d'une très lucrative association entre les deux partenaires depuis 1984.<br><br>
    Depuis 1984, les chaussures de sport estampillées Jordan se sont vendues à plus de 100 millions d'exemplaires à travers le monde.<br><br>
    Sur la vente de ces chaussures et produits dérivés provient l'essentiel des revenus de Michael Jordan qui percevait à une époque des royalties (redevance) de 6 % par paire vendue.<br><br>
    Depuis 2002, un événement est organisé annuellement, le Jordan Brand Classic Game, qui réunit les meilleurs lycéens et universitaires du pays mais également de nombreux joueurs européens et de championnats professionnels américains.<br><br>
    En outre, certains modèles sont créés exclusivement pour les magasins de la chaine Foot Locker et des modèles sortent en exclusivité dans les House of Hoops en partenariat avec Nike, la Jordan Brand fut le sponsor principal (avec Nike et Converse récemment racheté par la maison mère Nike) du championnat du monde de basket-ball en 2010.<br><br>
    La Jordan Brand 2 est le sponsor du Quai 54 à Paris depuis 2010, considéré comme l'un des plus prestigieux tournois de streetball au monde, Chris Paul et Ray Allen ont respectivement fait le déplacement pour représenter la marque et assister à l’événement.
    </p>
